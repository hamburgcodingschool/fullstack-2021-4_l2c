// VARIABLES act as containers for data

// Declaring a variable
let myAwesomeVariable;

// Assigning a value to a variable
myAwesomeVariable = "Hello World!";

// Initializing a variable
let myOtherVariable = "Goodbye World!";

console.log(myAwesomeVariable);
console.log(myOtherVariable);

myAwesomeVariable = "Some other text...";

console.log(myAwesomeVariable);
console.log(myOtherVariable);